#include <bits/stdc++.h>
using namespace std;

int add(int x, int y)
{
	return (x + y);
}

int main()
{
	/*
	FUNCTIONS:
	Monolithic Programming -> Everthing inside main.
	Modular/Procedural Programming -> Program consisting of several functions.
	*/
	int x, y, z;
	x = 10;
	y = 5;
	z = add(x, y);
	cout << "Sum x & y: " << z << "\n";

	return 0;
}

